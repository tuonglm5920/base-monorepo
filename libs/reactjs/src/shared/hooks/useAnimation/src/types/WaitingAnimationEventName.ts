/**
 * Represents the names of animation-related events, specifically for finishing and reversing animations.
 * This type is used to specify which event is being handled or listened to in the context of animations.
 *
 * - 'finish': Represents the event when an animation completes its cycle.
 * - 'reverseFinish': Represents the event when an animation completes its cycle in reverse.
 */
export type WaitingAnimationEventName = 'finish' | 'reverseFinish';
